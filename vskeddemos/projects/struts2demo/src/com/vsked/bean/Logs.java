package com.vsked.bean;

public class Logs {
	private int logId;
	private String logName;
	public int getLogId() {
		return logId;
	}
	public void setLogId(int logId) {
		this.logId = logId;
	}
	public String getLogName() {
		return logName;
	}
	public void setLogName(String logName) {
		this.logName = logName;
	}
	public Logs() {
		super();
	}
	

}
